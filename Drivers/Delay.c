/*---------------------INCLUDES----------------------*/
#include "Delay.h"

/*---------------------VARIABLES---------------------*/


/*---------------------FUNCTIONS---------------------*/
/***********************************************************************
** 函 数 名： delay10ms(void)
** 函数说明： 延迟10ms，与晶振有关,此处24Mhz
**---------------------------------------------------------------------
** 输入参数： 无
** 返回参数： 无
***********************************************************************/
void delay10ms(void)   //误差 -0.041666666667us
{
    unsigned char a,b,c;
    for(c=134;c>0;c--)
        for(b=12;b>0;b--)
            for(a=73;a>0;a--);
}
/***********************************************************************
** 函 数 名： delayus(unsigned int b)
** 函数说明： 延时程序-延时1us,该函数时间与晶振频率有关，此处为24Mhz,不精准
**---------------------------------------------------------------------
** 输入参数： uint32
** 返回参数： 无
***********************************************************************/
void delay1us(unsigned int b)
{
   while (b--)
   {
       _nop_();_nop_();_nop_();_nop_();_nop_(); _nop_();_nop_();_nop_();_nop_();_nop_();
	   _nop_();_nop_();_nop_();_nop_();_nop_(); _nop_();_nop_();_nop_();_nop_();_nop_();
	   _nop_();_nop_();_nop_();_nop_();
   }
}
/***********************************************************************
** 函 数 名： delayus()
** 函数说明： 延时程序-延时1s,该函数时间与晶振频率有关，此处为24Mhz
**---------------------------------------------------------------------
** 输入参数： 无
** 返回参数： 无
***********************************************************************/
void delay1s(void)   //误差 -0.000000000017us
{
    unsigned char a,b,c,n;
    for(c=205;c>0;c--)
        for(b=230;b>0;b--)
            for(a=253;a>0;a--);
    for(n=14;n>0;n--);
    _nop_();  
}